global.splitByNewline = (str, maxLength) => {
  var buff = '';
  var numOfLines = Math.floor(str.length / maxLength);
  for (var i = 0; i < numOfLines + 1; i++) {
    buff += str.substr(i * maxLength, maxLength);
    if (i !== numOfLines) {
      buff += '@';
    }
  }
  return buff;
};
